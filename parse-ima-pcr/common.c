#include <stdio.h>
#include <stdlib.h>
#include <stdint.h>
#include <stdbool.h>
#include <string.h>

unsigned char *
memdup(const unsigned char *mem, size_t size)
{
    if (!mem) {
        return NULL;
    }
    unsigned char *p = calloc(1, size);
    if (!p) {
        return NULL;
    }
    memcpy(p, mem, size);
    return p;
}